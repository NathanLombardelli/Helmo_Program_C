float ajoute(float nombre1,float nombre2)
{
    return nombre1+nombre2;
}

float soustrait(float nombre1,float nombre2)
{
    return nombre1-nombre2;
}

float multiplie(float nombre1,float nombre2)
{
    return nombre1*nombre2;
}

float divise(float nombre1,float nombre2)
{
    return nombre1/nombre2;
}
